    String columns[] = {"Course Id","Course Name", "Course Code"};
    private void fillstudentsTable(){

        courseDefaultTableModel=new DefaultTableModel(columns,0);

        List<Courses> getCourses=coursesDao.getAllCourses();
        for(Courses c: getCourses){

            Object row[]={c.getCourseId(),c.getName(),c.getCourseCode()};
            courseDefaultTableModel.addRow(row);

            courseTable.setModel(courseDefaultTableModel);
        }
    }
     private void clearFields(){
        courseNameField.setText("");
        courseCodeField.setText("");
    }
     
     private Courses getCoursesData(){
         String name=courseNameField.getText();
         String code=courseCodeField.getText();
         Courses courses=new Courses();
         courses.setName(name);
         courses.setCourseCode(code);
         return courses;
     }


