    private void courseUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_courseUpdateBtnActionPerformed
        // TODO add your handling code here:
        Courses courses=this.getCoursesData();
        courses.setCourseId(courseId);
        boolean success=coursesDao.updateCourses(courses);
        if(success){
            JOptionPane.showMessageDialog(this, "Updated Successfully");
            fillstudentsTable();
            clearFields();
        }
    }//GEN-LAST:event_courseUpdateBtnActionPerformed

    private void courseDeleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_courseDeleteBtnActionPerformed
        // TODO add your handling code here:
        Courses courses=this.getCoursesData();
        courses.setCourseId(courseId);
        boolean success=coursesDao.deleteCourses(courseId);
        if(success){
            JOptionPane.showMessageDialog(this, "Deleted Successfully");
            fillstudentsTable();
            clearFields();
        }
    }//GEN-LAST:event_courseDeleteBtnActionPerformed

